// Count words in a chapter array
function countWords(chapters) {
  var count = 0;
  for (var i = 0; i < chapters.length; i++) {
    var ch = chapters[i];
    count += ch.title.replace(/\s/g, '').length;
    if (Array.isArray(ch.content)) {
      for (var j = 0; j < ch.content.length; j++) {
        var item = ch.content[j];
        if (item.type === 'paragraph' || item.type === 'heading') {
          count += (item.text || '').replace(/\s/g, '').length;
        } else if (item.type === 'list') {
          for (var k = 0; k < (item.items || []).length; k++) {
            count += item.items[k].replace(/\s/g, '').length;
          }
        } else if (item.type === 'table') {
          for (var h = 0; h < (item.headers || []).length; h++) {
            count += item.headers[h].replace(/\s/g, '').length;
          }
          for (var r = 0; r < (item.rows || []).length; r++) {
            for (var c = 0; c < (item.rows[r] || []).length; c++) {
              count += String(item.rows[r][c]).replace(/\s/g, '').length;
            }
          }
        }
      }
    }
  }
  return count;
}

