// Generate expanded paragraphs based on chapter title
function generateExpansionContent(chapterTitle, currentWordCount, targetWordCount) {
  var target = targetWordCount;
  var current = currentWordCount;

  if (current >= target) return [];

  var needToAdd = target - current;
  var numParagraphs = Math.ceil(needToAdd / 120); // Each paragraph ~120 chars
  if (numParagraphs < 3) numParagraphs = 3;
  if (numParagraphs > 60) numParagraphs = 60;

  var content = [];
  content.push({ type: 'pageBreak' });

  // Generate content based on chapter keywords
  var title = chapterTitle;
  var paragraphs = [];

  if (title.indexOf('施工管理目标') >= 0 || title.indexOf('第一章') >= 0) {
    paragraphs = generateTargetContent();
  } else if (title.indexOf('施工组织部署') >= 0 || title.indexOf('第二章') >= 0) {
    paragraphs = generateDeployContent();
  } else if (title.indexOf('施工技术措施') >= 0 || title.indexOf('第三章') >= 0) {
    paragraphs = generateTechnicalContent();
  } else if (title.indexOf('质量') >= 0 && title.indexOf('第四章') >= 0) {
    paragraphs = generateQualityContent();
  } else if (title.indexOf('文明施工') >= 0 || title.indexOf('安全生产') >= 0 || title.indexOf('第五章') >= 0) {
    paragraphs = generateSafetyContent();
  } else if (title.indexOf('防尘降噪') >= 0 || title.indexOf('第六章') >= 0) {
    paragraphs = generateDustNoiseContent();
  } else if (title.indexOf('环境保护') >= 0 || title.indexOf('第七章') >= 0) {
    paragraphs = generateEnvironmentContent();
  } else if (title.indexOf('进度') >= 0 || title.indexOf('工期') >= 0 || title.indexOf('第八章') >= 0) {
    paragraphs = generateProgressContent();
  } else if (title.indexOf('机械配置') >= 0 || title.indexOf('第九章') >= 0) {
    paragraphs = generateMachineContent();
  } else if (title.indexOf('合理化建议') >= 0 || title.indexOf('降低成本') >= 0 || title.indexOf('第十章') >= 0) {
    paragraphs = generateSuggestContent();
  } else if (title.indexOf('通病防治') >= 0 || title.indexOf('第十一章') >= 0 || title.indexOf('防治措施') >= 0) {
    paragraphs = generateDiseaseContent();
  } else if (title.indexOf('应急响应时间') >= 0 || (title.indexOf('应急') >= 0 && title.indexOf('第一章') >= 0 && title.indexOf('时间') >= 0)) {
    paragraphs = generateEmergencyTimeContent();
  } else if (title.indexOf('故障处理') >= 0 || (title.indexOf('应急') >= 0 && title.indexOf('第二章') >= 0)) {
    paragraphs = generateEmergencyFaultContent();
  } else if (title.indexOf('改进方案') >= 0 || (title.indexOf('应急') >= 0 && title.indexOf('第三章') >= 0)) {
    paragraphs = generateEmergencyImproveContent();
  } else if (title.indexOf('售后服务响应') >= 0 || (title.indexOf('售后服务') >= 0 && title.indexOf('第一章') >= 0)) {
    paragraphs = generateServiceResponseContent();
  } else if (title.indexOf('售后服务人员') >= 0 || (title.indexOf('售后服务') >= 0 && title.indexOf('第二章') >= 0)) {
    paragraphs = generateServicePersonnelContent();
  } else if (title === '施工组织服务方案' || title === '应急响应预案' || title === '售后服务方案') {
    return [];
  }

  // Add the paragraphs as content items
  for (var i = 0; i < paragraphs.length && content.length <= numParagraphs + 1; i++) {
    content.push({
      type: 'paragraph',
      text: paragraphs[i],
      format: { firstLineIndent: 2 }
    });
  }

  return content;
}

